Esse novo segmento não tinha uma infraestrutura e formalidade de um supermercado, mas também não era baseado apenas na informalidade dos ambientes de feira. Ele era o melhor dos dois. E foi assim que dois jovens amigos montaram a primeira loja hortifrutigranjeira em Colatina, lá no interior do Espírito Santo, para transformar o sonho em realidade: oferecer produtos frescos, saborosos, coloridos e saudáveis ​​para as pessoas que chegavam até ali.


O desafio era enorme, mas a vontade também. Por isso revolucionaram tudo que puderam - desde a relação com os produtores e fornecedores, até a montagem e
revisado das bancas das lojas. A intenção era garantir sempre que as pessoas tivessem à disposição para levar para a casa o que existe de melhor na
natureza, tornando assim mais fácil a relação com uma alimentação saudável e sustentável.
